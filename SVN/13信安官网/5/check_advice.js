
      function check_advice()
      {
      var x=document.getElementById("advice").value;
      var x = x.replace(/(^\s*)|(\s*$)/g, "");
      if(x==""||x.length == 0)
        {
        alert("输入为空，请输入点建议吧");
        return false;
        }
        else{
        alert("谢谢你的建议！");
        return true;
        }
      }
